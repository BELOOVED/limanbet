<div class="contentTextTitle">Cashout (Bahis Bozdur) Nedir?</div>

<div class="contentText">
  
Karşılaşmaların sonuçlarını beklemeden, başlamamış ya da canlı karşılaşmalarda, tekli ve kombine kuponlarda kuponun tamamını ya da belirli bir kısmını bozdurmanız için kullanabileceğiniz, sitemize eklenmiş yeni bir özelliktir.
<br><br>
Örneğin 3 karşılaşmadan oluşan bir kupona 10 TL yatırdınız ve kupondaki ilk iki karşılaşma kazanmış olduğunu düşünelim. Son karşılaşmanın sonuçlanmasını beklemeden kuponunuzu bozdurabilir ve son karşılaşmanın bitmesini beklemeden kuponunuzu kazanca dönüştürebilirsiniz.
<br><br>
Aşağıda ki spor tiplerinde ve bahis marketlerinde yapmış olduğunuz kuponları bozdurabilirsiniz.

 <br><br>
Futbol : 3 İhtimalli, Beraberlikte İade, İki Takımda Gol Atar
<br><br>
Basketbol: 2 İhtimalli, 3 İhtimalli
<br><br>
Nasıl kullanılır?
<br><br>
Karşılaşmaya ait bahis tiplerinin yanında sembolü varsa, bu bahis tipi için yapılmış olan kuponlar bahis bozdurmaya elverişli demektir.
<br><br>
Kuponunuzu yaparken, oran değişikliğini kabul ediyorum seçeneğinin işaretli olduğuna emin olun. Eğer oran değişikliğine izin ver seçeneğini seçmişseniz, oran değiştikten sonra bile bahisinizi bozdurabilirsiniz, aksi takdirde bahis bozdurmaya izin verilmemektedir.
<br><br>
Eklemiş olduğunuz karşılaşmada bahis bozdur seçeneği aktif ise, kuponunuzda da karşılaşmanın yanında sembolü görebilirsiniz.
<br><br>
Eğer kuponunuza bahisbozdur seçeneği olmayan bir karşılaşmayı eklediyseniz, kuponunuz bahis bozdurmaya elverişli değil demektir.
<br><br>
Bahis geçmişinizde bahis bozdurmaya olanaklı kuponlarınızı ve önerilen miktarı görebilirsiniz.
<br><br>
Ayrıca kuponunuzdaki Geçmiş/Bahis Bozdur kısmından da bahis bozdurma alanine hızlıca geçiş yapabilirsiniz.
<br><br>
Bahis Bozdur’ da görülen miktarın tamamını ya da belirli bir kısmını bozdurabilirsiniz.
<br><br>
Kısmi bozdurma için sembolüne tıklanır ve aktif hale gelen kaydırma çubuğu ile bozdurulmak istenilen miktar seçilir.
<br><br>
Örneğin 10TL lik yapılmış olan bir kuponun 5TL’lik kısmının devam etmesini istediniz, bunun için, kaydırma çubuğunu %50 ye kadar sürüklemek ve Bahis Bozdur butonuna tıklamak yeterlidir.
<br><br>
Ardından karşınıza bir onay penceresi çıkacaktır ve onayladığınız takdirde, size bu kupon karşılığı önerilmiş olan 4.35TL yi alırsınız ve kuponunuz 5TL olarak devam eder.
<br><br>
Kupon eğer canlı bir bahise yapılmış karşılaşmayı içeriyorsa, son oran kontrol edilir ve eğer değişmişse bilgi verilir ve bahis bozdurma miktarı yeniden hesaplanarak yeni teklif sunulur.
<br><br>
Kupon için yatırdığınız ilk miktar ile bozdurma işleminden sonra kalan miktarı bahis geçmişinizde görebilirsiniz.
<br><br>
Tamamı bozdurulan bahislerde ise yatırılan ilk miktar gözükmekte ve kalan bahis miktarı “0” olarak gözükmektedir. Ayrıca Statü olarak da bahsin bozdurulduğu sembolü ile gösterilmektedir.
<br><br>
Finansal geçmişi altında gerçekleştirmiş olduğunuz bahis bozdurma işlemlerine ait miktarları görebilirsiniz.
<br><br>
Kurallar nelerdir?
<br><br>
