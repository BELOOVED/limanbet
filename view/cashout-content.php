Kupondaki herhangi bir bahis oynanıyorken askıya alındığında veya durduğunda bahis bozdurmaya izin verilmez
“Miktar değişikliğini kabul ediyorum” seçeneği işaretlenmediği takdirde, bahis bozdura başvurduğunuz zaman ile bahis bozdurun işleneceği zamanki miktarlar uyuşmuyor ise; bahis bozdurmaya izin verilmez. "Miktar değişimlerini kabul ediyorum" işaretlenir ise; o anki miktar ile bahis bozdurulur.
Kupondaki herhangi bir bahis marketinde bahis bozdurma aktif değil ise bahis bozdurmaya izin verilmez.
Kuponda kaybetmiş bir bahis var ise; bahis bozdurmaya izin verilmez.
Sitede bahisbozdur etkin durumda ve kupon cashout'a uygun olsa dahi bazı müşteriler cashout'dan muaf tutulabilir.
Yapılmış olan bahis bozdurma işlemi müşteri tarafından iptal edilemez.
Her bahis tipinde her zaman bahis bozdurma fonksiyonunu açık olmayabilir. Belirli marketleri ve belirli ligleri herhangi bir anda bahis bozdurma özelliğinden muaf tutabiliriz.<br><br>
Herhangi bir nedenle veya bahis bozdurma işlemi yapılmış bir kupondaki bir bahis tipinin veya karşılaşmanın iptal edilmesi durumunda, bozdurulan bahis miktarını geri alma hakkını saklı tutulmaktadır.

